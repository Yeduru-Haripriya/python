# Question 3 : Write a Python program that asks the user to enter an amount in their home currency (e.g., USD),
# asks for the target currency (e.g., EUR), uses predefined conversion rates to calculate the equivalent amount,
# and outputs the converted amount in the target currency.

currency_INR=float(input('Enter the amount in home currency '))
currency_target=input('Enter the target currency ').upper()

#taking some predefined rates not dynamic as the rates changes daily

rates_from_inr = {
    "USD": 0.0120,   # US Dollar
    "EUR": 0.0110,   # Euro
    "GBP": 0.0096,   # British Pound
    "JPY": 1.80,     # Japanese Yen
    "AUD": 0.0180,   # Australian Dollar
    "CAD": 0.0160,   # Canadian Dollar
    "SGD": 0.0162,   # Singapore Dollar
    "CHF": 0.0104,   # Swiss Franc
    "CNY": 0.0870,   # Chinese Yuan
}

if currency_target in rates_from_inr:
    conversion_rate = rates_from_inr[currency_target]
    converted_amount = currency_INR * conversion_rate
    print(f"{currency_INR:.2f} INR is {converted_amount:.2f} {currency_target}")
else:
    print("Invalid target currency")

# Question 4 : Write a Python program that asks for the user's weight (kg) and height (m),
# calculates the BMI , and outputs the BMI with a category (underweight, normal weight, overweight, or obese)
#  based on standard thresholds.

weight=float(input('Enter the weight in kg '))
height=float(input('Enter the height in m '))

bmi=weight/(height**2)

if bmi < 18.5:
        category = "Underweight"
elif bmi < 25:
        category = "Normal weight"
elif bmi < 30:
        category = "Overweight"
else:
        category = "Obese"

print(f"The BMI of the user is {bmi:.2f} and it comes under {category} category")

# Question 5 : Write a Python program to convert a user-input distance between miles and kilometers
# and output the converted distance using the conversion factors 1 mile = 1.60934 kilometers and 1 kilometer = 0.621371 miles.


dist=float(input('Enter the distance '))
unit=input('Enter the unit as m for miles and k for kilometers ').lower()

if unit=='m':
  converted_dist=dist*1.60934
  print(f"{dist:.2f} miles is {converted_dist:.4f} kilometers")

elif unit=='k':
  converted_dist=dist*0.621371
  print(f"{dist:.2f} kilometers is {converted_dist:.4f} miles")

else:
  print('Invalid unit')